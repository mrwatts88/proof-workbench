from __future__ import annotations

import json
import shutil
import subprocess
import sys
import tempfile
import unittest
from pathlib import Path


SOURCE_ROOT = Path(__file__).resolve().parents[1]


class ProofctlIntegrationTests(unittest.TestCase):
    def setUp(self) -> None:
        self.temporary = tempfile.TemporaryDirectory()
        self.root = Path(self.temporary.name) / "repo"
        shutil.copytree(
            SOURCE_ROOT,
            self.root,
            ignore=shutil.ignore_patterns(".git", "__pycache__", "*.pyc"),
        )
        problems = self.root / "problems"
        for path in problems.iterdir():
            if path.is_dir() and path.name != "_template":
                shutil.rmtree(path)
        (problems / "INDEX.md").write_text(
            "<!-- Generated by scripts/proofctl.py index. Do not edit by hand. -->\n"
            "# Problem index\n\nNo problem dossiers have been created.\n"
        )
        self.script = self.root / "scripts" / "proofctl.py"
        subprocess.run(
            [sys.executable, str(self.script), "--root", str(self.root), "index"],
            text=True,
            capture_output=True,
            check=True,
        )

    def tearDown(self) -> None:
        self.temporary.cleanup()

    def run_cli(
        self, *arguments: str, expected_returncode: int = 0
    ) -> subprocess.CompletedProcess[str]:
        result = subprocess.run(
            [
                sys.executable,
                str(self.script),
                "--root",
                str(self.root),
                *arguments,
            ],
            text=True,
            capture_output=True,
            check=False,
        )
        self.assertEqual(
            result.returncode,
            expected_returncode,
            msg=f"stdout:\n{result.stdout}\nstderr:\n{result.stderr}",
        )
        return result

    def test_empty_scaffold_validates(self) -> None:
        result = self.run_cli("validate")
        self.assertIn("Validation passed", result.stdout)

    def test_new_problem_records_and_status(self) -> None:
        self.run_cli(
            "new",
            "test-problem",
            "--title",
            "Test | Problem",
            "--statement",
            "For every integer n, n = n.",
        )
        self.run_cli("add", "test-problem", "session", "Initial audit")
        self.run_cli("add", "test-problem", "attempt", "Direct proof")
        self.run_cli(
            "set-status",
            "test-problem",
            "active",
            "--claim",
            "open",
            "--next",
            "Prove C001 from reflexivity.",
        )
        self.run_cli("validate")

        manifest = json.loads(
            (self.root / "problems" / "test-problem" / "problem.json").read_text()
        )
        self.assertEqual(manifest["id"], "P-001")
        self.assertEqual(manifest["work_status"], "active")
        self.assertTrue(
            list(
                (self.root / "problems" / "test-problem" / "sessions").glob(
                    "S001-*.md"
                )
            )
        )
        index = (self.root / "problems" / "INDEX.md").read_text()
        self.assertIn("Test \\| Problem", index)
        dashboard = (self.root / "README.md").read_text()
        self.assertIn("Previously proven?", dashboard)
        self.assertIn("Unknown", dashboard)

    def test_completion_gate_rejects_unreviewed_candidate(self) -> None:
        self.run_cli("new", "gate-test", "--title", "Gate test")
        result = self.run_cli(
            "set-status",
            "gate-test",
            "complete",
            "--claim",
            "proved",
            expected_returncode=2,
        )
        self.assertIn("unresolved obligations", result.stderr)
        self.assertIn("requires 2 review record", result.stderr)
        self.assertIn("integrated candidate", result.stderr)

        manifest = json.loads(
            (self.root / "problems" / "gate-test" / "problem.json").read_text()
        )
        self.assertEqual(manifest["work_status"], "intake")
        self.assertEqual(manifest["claim_status"], "open")

    def test_review_command_requires_and_enters_candidate_review(self) -> None:
        self.run_cli("new", "review-test", "--title", "Review test")
        result = self.run_cli(
            "review",
            "review-test",
            "Logic audit",
            "--type",
            "logic",
            expected_returncode=2,
        )
        self.assertIn("only after claim_status", result.stderr)

        self.run_cli(
            "set-status", "review-test", "candidate", "--claim", "proof_candidate"
        )
        self.run_cli("review", "review-test", "Logic audit", "--type", "logic")
        manifest = json.loads(
            (self.root / "problems" / "review-test" / "problem.json").read_text()
        )
        self.assertEqual(manifest["work_status"], "review")
        review = next(
            (self.root / "problems" / "review-test" / "reviews").glob("R001-*.md")
        )
        self.assertIn("Review type: logic", review.read_text())

    def test_review_status_requires_a_review_record(self) -> None:
        self.run_cli("new", "review-gate", "--title", "Review gate")
        self.run_cli(
            "set-status", "review-gate", "candidate", "--claim", "proof_candidate"
        )
        result = self.run_cli("set-status", "review-gate", "review", expected_returncode=2)
        self.assertIn("requires at least one review record", result.stderr)

    def test_stale_index_is_detected(self) -> None:
        self.run_cli("new", "index-test", "--title", "Index test")
        index_path = self.root / "problems" / "INDEX.md"
        index_path.write_text(index_path.read_text() + "stale\n")
        result = self.run_cli("validate", expected_returncode=1)
        self.assertIn("INDEX.md is stale", result.stderr)

    def test_stale_readme_dashboard_is_detected(self) -> None:
        self.run_cli("new", "dashboard-test", "--title", "Dashboard test")
        readme_path = self.root / "README.md"
        readme_path.write_text(
            readme_path.read_text().replace("<!-- END GENERATED", "<!-- STALE GENERATED")
        )
        result = self.run_cli("validate", expected_returncode=1)
        self.assertIn("README.md problem dashboard is stale", result.stderr)

    def test_claude_entry_point_must_import_the_contract(self) -> None:
        claude_path = self.root / "CLAUDE.md"
        self.assertIn("@AGENTS.md", claude_path.read_text())
        claude_path.write_text("# Claude Code\n\nA forked copy of the contract.\n")
        result = self.run_cli("validate", expected_returncode=1)
        self.assertIn("CLAUDE.md must import the contract", result.stderr)

    def test_strategy_audit_is_scaffolded_and_guarded(self) -> None:
        self.run_cli("new", "strategy-test", "--title", "Strategy test")
        self.run_cli("add", "strategy-test", "session", "Route selection")
        self.run_cli("add", "strategy-test", "attempt", "Alternative mechanism")

        session = next(
            (self.root / "problems" / "strategy-test" / "sessions").glob(
                "S001-*.md"
            )
        ).read_text()
        attempt = next(
            (self.root / "problems" / "strategy-test" / "attempts").glob(
                "A001-*.md"
            )
        ).read_text()
        state = (self.root / "problems" / "strategy-test" / "STATE.md").read_text()
        self.assertIn("## Strategy audit", session)
        self.assertIn("- Portfolio role:", attempt)
        self.assertIn("## Strategy portfolio", state)

        template_path = self.root / "templates" / "session.md"
        template_path.write_text(
            template_path.read_text().replace("## Strategy audit", "## Route notes")
        )
        result = self.run_cli("validate", expected_returncode=1)
        self.assertIn("missing required process marker", result.stderr)

    def test_review_records_how_independence_was_obtained(self) -> None:
        self.run_cli("new", "independence-test", "--title", "Independence test")
        self.run_cli(
            "set-status", "independence-test", "candidate", "--claim", "proof_candidate"
        )
        result = self.run_cli(
            "review",
            "independence-test",
            "Logic audit",
            "--type",
            "logic",
            "--independence",
            "same-context-limited",
        )
        self.assertIn("WARNING", result.stdout)
        review = next(
            (self.root / "problems" / "independence-test" / "reviews").glob("R001-*.md")
        )
        self.assertIn("Independence mode: same-context-limited", review.read_text())

    def test_explicit_record_id_allocation_for_parallel_sessions(self) -> None:
        """`--id` pre-allocates record numbers so concurrent sessions cannot collide.

        Two sessions working the same dossier in separate worktrees each scan
        their own tree for the next free number and would both get the same one;
        `process/concurrency.md` requires the IDs to be handed out in advance.
        """
        self.run_cli("new", "parallel-target", "--title", "Parallel Target")

        # Explicit allocation skips ahead of the next free number.
        self.run_cli("add", "parallel-target", "attempt", "Route one", "--id", "A007")
        attempts = self.root / "problems" / "parallel-target" / "attempts"
        self.assertTrue(list(attempts.glob("A007-*.md")))

        # The prefixed spelling is accepted too, and a second session's
        # independently allocated ID does not disturb the first.
        self.run_cli("add", "parallel-target", "attempt", "Route two", "--id", "008")
        self.assertTrue(list(attempts.glob("A008-*.md")))

        # A used ID is refused rather than silently overwritten.
        refused = self.run_cli(
            "add",
            "parallel-target",
            "attempt",
            "Collision",
            "--id",
            "A007",
            expected_returncode=2,
        )
        self.assertIn("already used", refused.stderr + refused.stdout)

        # Malformed IDs are refused.
        malformed = self.run_cli(
            "add",
            "parallel-target",
            "attempt",
            "Bad id",
            "--id",
            "A7",
            expected_returncode=2,
        )
        self.assertIn("three digits", malformed.stderr + malformed.stdout)

        # Automatic allocation still works and continues past the explicit ones.
        self.run_cli("add", "parallel-target", "attempt", "Route three")
        self.assertTrue(list(attempts.glob("A009-*.md")))
        self.run_cli("validate")

    def test_operation_record_does_not_touch_problem_dashboard(self) -> None:
        index_before = (self.root / "problems" / "INDEX.md").read_text()
        self.run_cli("operation", "Refresh tooling")
        self.run_cli("validate")
        self.assertEqual(index_before, (self.root / "problems" / "INDEX.md").read_text())
        self.assertTrue(list((self.root / "operations").glob("O[0-9][0-9][0-9]-*.md")))


if __name__ == "__main__":
    unittest.main()
